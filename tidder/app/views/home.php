				<h1>O que significa API e como ela funciona!</h1>
				<p>A API é um conjunto de definições e protocolos usado no desenvolvimento e na integração de software
					de aplicações. API é um acrônimo em inglês que significa interface de programação de aplicações.
					Com as APIs, sua solução ou serviço pode se comunicar com outros produtos e serviços sem precisar
					saber como eles foram implementados. Isso simplifica o desenvolvimento de aplicações,
					gerando economia de tempo e dinheiro. Ao desenvolver novas ferramentas e soluções 
					(ou ao gerenciar aquelas já existentes), as APIs oferecem a flexibilidade necessária para simplificar
					o design, a administração e o uso, além de fornecer oportunidades de inovação.</p>

				<p><a class="btn btn-primary btn-lg" data-toggle="collapse" href="#collapseExample" role="button" aria-expanded="false" aria-controls="collapseExample">Leia mais</a></p>
