            <p> No ano de 2019, um vírus se espalhou por toda a China e nesse momento se espalha por todo o mundo.
                A Organização Mundial da Saúde (OMS) já declarou que o surto da doença constitui uma Emergência de Saúde Pública de Importância Internacional e também como uma pandemia.
                
                Foram confirmados no mundo mais de 5 milhões de casos de COVID-19 e mais de 330 mil mortes até o momento.
                As medidas de proteção devem ser feitas como: lavar as mãos frequentemente com água e sabão, usar álcool em gel e cobrir a boca com o 
                antebraço quando tossir ou espirrar (ou use um lenço descartável e, após tossir e/ou espirrar, jogue no lixo e lave as mãos).
                Se uma pessoa tiver sintomas menos agravantes, como tosse leve ou febre leve, geralmente não há necessidade de procurar atendimento médico. 
                O ideal é ficar em casa, fazer autoisolamento e monitorar os sintomas. 
                Procure atendimento médico imediato se tiver dificuldade de respirar ou dor/pressão no peito.
                Caso queira saber mais sobre o COVID-19 e como se previnir, entre no site do <a href="https://coronavirus.saude.gov.br/">Ministério da Saúde</a> para saber mais.<br><br>
                    
                Sendo assim, como muitas APIs foram criadas e/ou melhoradas para ajudar a combate-lo, ou que estão relacionadas a ele de alguma forma, decidimos demonstrar abaixo algumas dessas principais APIs.</p>
